
public class Variable {
	public static void main(String[] args) {
		//integer 정수 
		int a = 1;
		System.out.println(a);
		
		//real number 실수 double
		double b = 1.1;
		System.out.println(b);
		
		//string 문자열
		String c = "Hello world";
		System.out.println(c);
		
		//char
		char d = 'd';
		System.out.println(d);
	}
}
