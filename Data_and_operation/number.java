
public class number {

	public static void main(String[] args) {
		//operator연산자
		System.out.println(6+2); //8
		System.out.println(6-2); //4
		System.out.println(6*2); //12
		System.out.println(6/2); //3
		
		System.out.println(Math.PI); //원주
		System.out.println(Math.floor(Math.PI)); //소주점내림
		System.out.println(Math.ceil(Math.PI));  //소수점올
		System.out.println(Math.round(Math.PI)); //반올림
		System.out.println(Math.max(5, 6));
	}

}
